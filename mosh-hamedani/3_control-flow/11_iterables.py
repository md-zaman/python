for x in range(5):
    print(x)

# range type is iterable. We can iterate over it. In each iteration x will have aa different value
# It means that we can write code like above with for loop

for x in "Python": #strings are also iterable
    print(x)

for x in [1, 2, 3, 4]: #This is a list. Also iterable
    print(x)


    